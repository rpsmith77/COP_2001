#include "point.h"
//-------------------------------------------------------------------------
// constructors
//-------------------------------------------------------------------------
/**************************************************************************
default constructor
**************************************************************************/
Point::Point()
{
	x = 0.0f;
	y = 0.0f;
}					
/**************************************************************************
property constructor
**************************************************************************/
Point::Point(float x, float y)
{
	this->x = x;
	this->y = y;
}

/**************************************************************************
copy constructor
**************************************************************************/
Point::Point(Point* point)
{
	x = point->x;
	y = point->y;
}

//-------------------------------------------------------------------------
// accessors (getters & setters)
//-------------------------------------------------------------------------
float Point::getX() { return x; }
void  Point::setX(float value) { x = value; }
float Point::getY() { return y; }
void  Point::setY(float value) { y = value; }

//-------------------------------------------------------------------------
// member methods
//-------------------------------------------------------------------------
/******************************************************************************
create a line with point-slope form
parameters:
	none
returns:
	bool		- true if both values are zero, false if either value is
				  non-zero
******************************************************************************/
bool Point::isEmpty()
{
	return !(x || y);
}